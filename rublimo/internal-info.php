  <h3><b>Займ без поручителей</b></h3>
  <p>Поручительство – ответственный шаг, на который отважится не каждый знакомый. Именно поэтому займы без поручителей являются
    одной из наиболее востребованных форм кредитования, поскольку для их получения не нужно привлекать третье лицо. Почему
    люди не хотят поручиться за должника?</p>

  <p>Микрофинансовой организации не важно кто будет платить по займу – заемщик или его поручитель. Вместе с тем, последнему
    не хочется быть обманутым и погашать чужие долги. Даже если Вы надежный и порядочный заемщик, Ваш гарант становится ответчиком
    и может оценить риски крайне негативно.</p>

  <p><b>Не тратьте время на поиски третьего лица, и оформите кредит самостоятельно!</b></p>

  <p><b>Выгоды заема без поручителей:</b></p>

  <ul>
    <li>Существенная экономия времени заемщика.</li>
    <li>Отсутствие необходимости согласования времени прибытия в офис.</li>
    <li>Сокращение списка требуемых документов до минимума.</li>
