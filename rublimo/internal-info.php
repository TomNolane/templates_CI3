  <h3><b>Банки наживаются на клиентах</b></h3>
  <p>Ни для кого не секрет, что банки создаются с одной единственной целью, которая заключается не в оказании помощи людям,
    как вы могли подумать, а в получении как можно большей прибыли всеми доступными, а нередко и незаконными способами. Конечно,
    это не означает что нужно полностью отказаться от услуг всевозможных кредитных организаций — к сожалению, в нашем современном
    мире это практически невозможно даже будучи миллиардером. Итак, на что идут банки ради получения денег со своих клиентов
    мы расскажем далее на примере трех наиболее популярных «разводок».</p>

  <p><b>Непогашение кредита</b></p>

  <p>Уловка в данном случае заключается в том, что клиент банка, внеся последний платёж по кредиту, предусмотренный графиком
    погашения задолженности, благополучно о нём забывает. Однако зачастую человек при этом гасит не весь долг, оказывается
    он остается должен банку незначительную сумму – рублей тридцать. Далее банк на мизерный долг начинает накручивать проценты,
    штрафы, пени и так далее. Об этом вы знать не будете, всё происходит втайне от клиента. Проходит почти три года (три
    года – срок исковой давности, в течение которого банк может обратиться в суд для взыскания с клиента образовавшегося
    долга), а банк к удивлению человека уже забывшего о кредите требует окончательного его погашения. Отметим, что речь уже
    будет идти не о тридцати рублях, а о нескольких тысячах рублей, и то если повезет.</p>

  <p>В одном банке часто возникали случаи, когда людям, расплатившимся за кредит, звонили через полгода и говорили, что на момент
    погашения они остались должны две тысячи рублей, а сейчас эта сумма возросла до 35 000 рублей. Получается, что таинственная
    сумма выдумывается работниками кредитной организации для каждого клиента отдельно (вот что называется персональный подход
    к каждому клиенту!). Конечно, человек будет утверждать, что он весь кредит выплатил (если это на самом деле правда),
    но иногда доказать это невозможно, так как квитанции об оплате были им благополучно выброшены. Если же вы догадались
    сохранить все подтверждающие документы и в случае претензии предъявили их банку, то сотрудники кредитной организации
    спишут всё на компьютерный сбой и, скрепя сердце, вас отпустят.</p>

  <p><b>Как с этим бороться?</b></p>

  <p>Оказывается, очень просто. Вам всего-то и нужно, перечислив последний платеж, потребовать справку о том, что кредит полностью
    погашен. Также не лишним будет хранить у себя все квитанции об оплате, хотя бы три года (помните про исковую давность).</p>
