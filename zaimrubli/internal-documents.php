                                                    </p>
                                                    <h4>
                                                        3 ОРГАНИЗАЦИЯ ОБРАБОТКИ ПЕРСОНАЛЬНЫХ ДАННЫХ
                                                    </h4>
                                                    <h4>
                                                        3.1 Назначение ответственных лиц
                                                    </h4>
                                                    <p>
                                                        Для организации обработки персональных данных у Оператора назначается ответственное лицо.
                                                    </p>
                                                    <p>
                                                        Для определения уровня защищенности информационных систем персональных данных, проверки готовности средств защиты информации
                                                        к использованию, а также уничтожения персональных данных приказом
                                                        руководителя Оператора назначается Комиссия по приведению в соответствие
                                                        с требованиями законодательства Российской Федерации в области персональных
                                                        данных (далее – Комиссия).
                                                    </p>
                                                    <p>
                                                        В своей работе Комиссия руководствуется Положением о комиссии по приведению в соответствие с требованиями законодательства
                                                        Российской Федерации в области персональных данных, утвержденным
                                                        приказом руководителя Оператора.
                                                    </p>
                                                    <h4>
                                                        3.2 Допуск работников к обработке персональных данных
                                                    </h4>
                                                    <p>
                                                        Допуск работников Оператора к обработке персональных данным осуществляется на основании приказа о назначении на должность
                                                        в соответствии с Перечнем должностей и третьих лиц, имеющих доступ
                                                        к персональным данным.
                                                    </p>
                                                    <p>
                                                        Работники Оператора получают доступ к обработке персональных данных для выполнения ими служебных (трудовых) обязанностей,
                                                        после выполнения следующих мероприятий:
                                                    </p>
                                                    <p>
                                                        ознакомления под роспись с руководящими документами Оператора и нормативными актами Российской Федерации по обработке и обеспечению
                                                        безопасности персональных данных;
                                                    </p>
                                                    <p>
                                                        оформления письменного обязательства о неразглашении персональных данных, форма которого утверждена приказом руководителя
                                                        Оператора.
                                                    </p>
                                                    <p>
                                                        Работники Оператора, имеющие допуск к персональным данным, имеют право получать только те персональные данные, которые необходимы
                                                        им для выполнения служебных (трудовых) обязанностей.
                                                    </p>
                                                    <h4>
                                                        3.3 Получение персональных данных
                                                    </h4>
                                                    <p>
                                                        Персональные данные субъекта получаются от него самого или от него законного представителя. В случае, если персональные данные
                                                        получены не от субъекта персональных данных, Оператор до начала обработки
                                                        таких персональных данных обязан уведомить субъект о получении его
                                                        персональных данных.
                                                    </p>
                                                    <h4>
                                                        3.4 Систематизация, накопление, уточнение и использование персональных данных
                                                    </h4>
                                                    <p>
                                                        Систематизация, накопление, уточнение и использование персональных данных осуществляется путем оформления и ведения документов
                                                        учета и баз данных субъектов персональных данных.
                                                    </p>
                                                    <p>
                                                        Работники Оператора, имеющие доступ к персональным данным, должны обеспечить их обработку, исключающую несанкционированный
                                                        доступ к ним третьих лиц.
                                                    </p>
                                                    <h4>
                                                        3.5 Передача персональных данных
                                                    </h4>
                                                    <p>
                                                        Передача персональных данных субъектов третьим лицам может осуществляться только при наличии письменного согласия субъекта,
                                                        если иное не предусмотрено федеральным законодательством.
                                                    </p>
                                                    <p>
                                                        При передаче персональных данных субъектов третьим лицам, с третьим лицом должно быть подписано Соглашение о соблюдении безопасности
                                                        персональных данных, переданных на обработку, форма которого утверждена
                                                        приказом руководителя Оператора.
                                                    </p>
                                                    <p>
                                                        Передача персональных данных субъектов между подразделениями Оператора должна осуществляться только между работниками, допущенными
                                                        к обработке персональных данных.
                                                    </p>
                                                    <h4>
                                                        3.6 Хранение персональных данных
                                                    </h4>
                                                    <p>
                                                        Хранение персональных данных субъектов осуществляется на бумажных и машинных носителях информации в специально выделенных
                                                        хранилищах подразделений Оператора, а также в информационных системах
                                                        Оператора, обеспечивающих сохранность персональных данных и их защиту
                                                        от несанкционированного доступа.
                                                    </p>
                                                    <p>
                                                        Уничтожение персональных данных в информационных системах, на машинных и бумажных носителях информации должно производиться
                                                        в течение тридцати дней с даты достижения цели обработки (предельного
                                                        срока хранения) персональных данных. При невозможности уничтожения
                                                        персональных данных в течение тридцати дней с даты достижения цели
                                                        обработки персональных данных, обеспечивается их блокирование и уничтожение
                                                        в срок, не превышающий шести месяцев.
                                                    </p>
                                                    <p>
                                                        Порядок и правила учета, хранения и уничтожения персональных данных описаны в Регламенте по учету, хранению и уничтожению
                                                        носителей персональных данных.
                                                    </p>
                                                    <h4>
                                                        3.7 Уведомление об обработке персональных данных
                                                    </h4>
                                                    <p>
                                                        Согласно ст. 22 ФЗ152 Оператор уведомляет Уполномоченный орган по защите прав субъектов персональных данных об обработке
                                                        персональных данных.
                                                    </p>
                                                    <p>
                                                        В случае изменения сведений, указанных в уведомлении, а также в случае прекращения обработки персональных данных Оператор
                                                        также уведомляет об этом Уполномоченный орган.
                                                    </p>
                                                    <h4>
                                                        4 ОСОБЕННОСТИ ОРГАНИЗАЦИИ ОБРАБОТКИ ПЕРСОНАЛЬНЫХ ДАННЫХ, ОСУЩЕСТВЛЯЕМОЙ БЕЗ ИСПОЛЬЗОВАНИЯ СРЕДСТВ АВТОМАТИЗАЦИИ
                                                    </h4>
                                                    <p>
                                                        Персональные данные при их обработке без использования средств автоматизации обособляются от иной информации путем фиксации
                                                        их на отдельных материальных носителях персональных данных, в специальных
                                                        разделах или на полях форм (бланков).
                                                    </p>
                                                    <p>
                                                        При фиксации персональных данных на материальных носителях не допускается запись на одном материальном носителе персональных
                                                        данных, цели обработки которых заведомо несовместимы. При обработке
                                                        различных категорий персональных данных без использования средств
                                                        автоматизации для каждой категории персональных данных должен использоваться
                                                        отдельный материальный носитель.
                                                    </p>
                                                    <p>
                                                        При использовании типовых форм документов, характер информации в которых предполагает или допускает включение в них персональных
                                                        данных, должны соблюдаться следующие условия:
                                                    </p>
                                                    <p>
                                                        типовая форма должна содержать сведения о цели обработки персональных данных, наименование и адрес Оператора, фамилию, имя,
                                                        отчество и адрес субъекта персональных данных, источник получения
                                                        персональных данных, сроки обработки персональных данных, перечень
                                                        действий с персональными данными, которые будут совершаться в процессе
                                                        их обработки, общее описание используемых оператором способов обработки
                                                        персональных данных;
                                                    </p>
                                                    <p>
                                                        типовая форма должна предусматривать поле, в котором субъект персональных данных может поставить отметку о своем согласии
                                                        на обработку персональных данных – при необходимости получения письменного
                                                        согласия на обработку персональных данных;
                                                    </p>
                                                    <p>
                                                        типовая форма должна быть составлена таким образом, чтобы каждый из субъектов персональных данных, содержащихся в документе,
                                                        имел возможность ознакомиться со своими персональными данными, содержащимися
                                                        в документе, не нарушая прав и законных интересов иных субъектов
                                                        персональных данных;
                                                    </p>
                                                    <p>
                                                        типовая форма должна исключать объединение полей, предназначенных для внесения персональных данных, цели обработки которых
                                                        заведомо не совместимы.
                                                    </p>
                                                    <p>
                                                        При несовместимости целей обработки персональных данных, зафиксированных на одном материальном носителе, если материальный
                                                        носитель не позволяет осуществлять обработку персональных данных
                                                        отдельно от других зафиксированных на том же носителе персональных
                                                        данных, должны быть приняты меры по обеспечению раздельной обработки
                                                        персональных данных.
                                                    </p>
                                                    <p>
                                                        Необходимо обеспечивать раздельное хранение персональных данных (материальных носителей), обработка которых осуществляется
                                                        в различных целях.
                                                    </p>
                                                    <p>
                                                        Уничтожение или обезличивание части персональных данных, если это допускается материальным носителем, может производиться
                                                        способом, исключающим дальнейшую обработку этих персональных данных
                                                        с сохранением возможности обработки иных данных, зафиксированных
                                                        на материальном носителе.
                                                    </p>
                                                    <p>
                                                        Уточнение персональных данных при их обработке без использования средств автоматизации производится путем обновления или
                                                        изменения данных на материальном носителе, а если это не допускается
