                                                    <h4>
                                                        1 ОБЩИЕ ПОЛОЖЕНИЯ
                                                    </h4>
                                                    <p>
                                                        Положение об обработке персональных данных в ООО «Альянс» (далее – Положение) разработано в соответствии с Федеральным законом
                                                        от 27.07.2006 № 152ФЗ «О персональных данных» (далее – ФЗ152), Трудовым
                                                        кодексом Российской Федерации (далее – ТК РФ), а также «Перечнем
                                                        сведений конфиденциального характера», утвержденным Указом Президента
                                                        Российской Федерации от 06.03.1997 № 188.
                                                    </p>
                                                    <p>
                                                        Настоящее Положение определяет порядок обработки персональных данных и устанавливает общие требования к обеспечению безопасности
                                                        персональных данных, обрабатываемых в ООО «Альянс» (далее – Оператор)
                                                        как с использованием средств автоматизации, так и без использования
                                                        таких средств.
                                                    </p>
                                                    <p>
                                                        В Положении используются следующие основные понятия:
                                                    </p>
                                                    <p>
                                                        <strong>автоматизированная обработка персональных данных</strong>
                                                        – обработка персональных данных с помощью средств вычислительной техники;
                                                    </p>
                                                    <p>
                                                        <strong>блокирование персональных данных</strong>
                                                        – временное прекращение обработки персональных данных (за исключением случаев, если обработка необходима для уточнения персональных
                                                        данных);
                                                    </p>
                                                    <p>
                                                        <strong>информационная система персональных данных</strong>
                                                        – совокупность содержащихся в базах данных персональных данных и обеспечивающих их обработку информационных технологий и
                                                        технических средств;
                                                    </p>
                                                    <p>
                                                        <strong>обезличивание персональных данных</strong>
                                                        <em> – </em>
                                                        действия, в результате которых невозможно определить без использования дополнительной информации принадлежность персональных
                                                        данных конкретному субъекту персональных данных;
                                                    </p>
                                                    <p>
                                                        <strong>обработка персональных данных</strong>
                                                        – любое действие (операция) или совокупность действий (операций), совершаемых с использованием средств автоматизации или
                                                        без использования таких средств с персональными данными, включая
                                                        сбор, запись, систематизацию, накопление, хранение, уточнение (обновление,
                                                        изменение), извлечение, использование, передачу (распространение,
                                                        предоставление, доступ), обезличивание, блокирование, удаление, уничтожение
                                                        персональных данных;
                                                    </p>
                                                    <p>
                                                        <strong>оператор – </strong>
                                                        государственный орган, муниципальный орган, юридическое или физическое лицо, самостоятельно или совместно с другими лицами
                                                        организующие и (или) осуществляющие обработку персональных данных,
                                                        а также определяющие цели обработки персональных данных, состав персональных
                                                        данных, подлежащих обработке, действия (операции), совершаемые с
                                                        персональными данными;
                                                    </p>
                                                    <p>
                                                        <strong>персональные данные</strong>
                                                        – любая информация, относящаяся к прямо или косвенно определенному или определяемому физическому лицу (субъекту персональных
                                                        данных);
                                                    </p>
                                                    <p>
                                                        <strong>предоставление персональных данных</strong>
                                                        – действия, направленные на раскрытие персональных данных определенному лицу или определенному кругу лиц;
                                                    </p>
                                                    <p>
                                                        <strong>распространение персональных данных</strong>
                                                        – действия, направленные на раскрытие персональных данных неопределенному кругу лиц (передача персональных данных) или на
                                                        ознакомление с персональными данными неограниченного круга лиц, в
                                                        том числе обнародование персональных данных в средствах массовой
                                                        информации, размещение в информационно-телекоммуникационных сетях
                                                        или предоставление доступа к персональным данным каким-либо иным
                                                        способом;
                                                    </p>
                                                    <p>
                                                        <strong>трансграничная передача персональных данных</strong>
                                                        – передача персональных данных на территорию иностранного государства органу власти иностранного государства, иностранному
                                                        физическому или иностранному юридическому лицу;
                                                    </p>
                                                    <p>
                                                        <strong>уничтожение персональных данных</strong>
                                                        – действия, в результате которых невозможно восстановить содержание персональных данных в информационной системе персональных
                                                        данных и (или) в результате которых уничтожаются материальные носители
                                                        персональных данных.
                                                    </p>
                                                    <p>
                                                        Действие Положения распространяется на все структурные подразделения Оператора.
                                                    </p>
                                                    <p>
                                                        Настоящее Положение должно быть доведено до каждого работника Оператора, осуществляющего обработку персональных данных, под
                                                        роспись.
                                                    </p>
                                                    <h4>
                                                        2 СУБЪЕКТЫ И ЦЕЛИ ОБРАБОТКИ ПЕРСОНАЛЬНЫХ ДАННЫХ
