            <li>
                <p align="LEFT">
                    <strong>Персональные данные (ПДн)</strong>
                    – любая информация, относящаяся к прямо или косвенно определенному или определяемому физическому лицу (субъекту персональных
                    данных).
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Оператор</strong>
                    – государственный орган, муниципальный орган, юридическое или физическое лицо, самостоятельно или совместно с другими лицами
                    организующие и (или) осуществляющие обработку персональных данных, а также определяющие цели обработки
                    персональных данных, состав персональных данных, подлежащих обработке, действия (операции), совершаемые
                    с персональными данными.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Обработка персональных данных</strong>
                    – любое действие (операция) или совокупность действий (операций), совершаемых с использованием средств автоматизации или
                    без использования таких средств с персональными данными, включая сбор, запись, систематизацию, накопление,
                    хранение, уточнение (обновление, изменение), извлечение, использование, передачу (распространение, предоставление,
                    доступ), обезличивание, блокирование, удаление, уничтожение персональных данных.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Автоматизированная обработка персональных данных</strong>
                    - обработка персональных данных с помощью средств вычислительной техники.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Распространение персональных данных</strong>
                    – действия, направленные на раскрытие персональных данных неопределенному кругу лиц.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Предоставление персональных данных</strong>
                    - действия, направленные на раскрытие персональных данных определенному лицу или определенному кругу лиц.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Блокирование персональных данных</strong>
                    – временное прекращение обработки персональных данных (за исключением случаев, если обработка необходима для уточнения персональных
                    данных).
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Уничтожение персональных данных</strong>
                    – действия, в результате которых становится невозможным восстановить содержание персональных данных в информационной системе
                    персональных данных и (или) в результате которых уничтожаются материальные носители персональных данных.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Обезличивание персональных данных</strong>
                    – действия, в результате которых становится невозможным без использования дополнительной информации определить принадлежность
                    персональных данных конкретному субъекту персональных данных.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Информационная система персональных данных (ИСПДн)</strong>
                    – совокупность содержащихся в базах данных персональных данных и обеспечивающих их обработку информационных технологий и
                    технических средств.
                </p>
            </li>
            <li>
                <p align="LEFT">
                    <strong>Трансграничная передача персональных данных</strong>
                    - передача персональных данных на территорию иностранного государства органу власти иностранного государства, иностранному
                    физическому лицу или иностранному юридическому лицу.
                </p>
            </li>
        </ul>
