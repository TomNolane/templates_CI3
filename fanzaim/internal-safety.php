                </p>
                <p align="LEFT">
                    4.3 Задачи по приведению деятельности Оператора в соответствие с требованиями законодательства Российской Федерации в области
                    ПДн возлагаются на специально создаваемую для этих целей Комиссию и лиц, ответственных за организацию
